public class Printnumbacke {
    
        public static void main(String args[]) {
            int N = 5;
            // 'n--' increment help to increament backward //
            for ( int iter = N; iter > 0; iter-- ) {
                //we can use only 'print' to  show it in a single line//
                System.out.println(iter + " ");
            }
        }
    
}
